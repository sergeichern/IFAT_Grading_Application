    private void btn_CustomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CustomActionPerformed
        try {
            weights = new Hashtable<>();
            int firstAtt = Integer.parseInt(txt_Att1.getText());
            int secondAtt = Integer.parseInt(txt_Att2.getText());
            int thirdAtt = Integer.parseInt(txt_Att3.getText());
            int fourthAtt = Integer.parseInt(txt_Att4.getText());
            int fifthAtt = Integer.parseInt(txt_Att5.getText());

            weights.put("Attempt 1", firstAtt);
            weights.put("Attempt 2", secondAtt);
            weights.put("Attempt 3", thirdAtt);
            weights.put("Attempt 4", fourthAtt);
            weights.put("Attempt 5", fifthAtt);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_CustomActionPerformed

