    private void btn_GradeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GradeActionPerformed
        Enumeration<String> en = studentCardTable.keys();
        if (weights == null) {
            weights = new Hashtable<>();
            weights.put("Attempt 1", 100); // attempt 1 gets 100%
            weights.put("Attempt 2", 66); // attempt 2 gets 66%
            weights.put("Attempt 3", 33); // attempt 3 gets 33%
            weights.put("Attempt 4", 25); // attempt 4 gets 25%
            weights.put("Attempt 5", 0); // attempt 5 gets 0%
        }
        while (en.hasMoreElements()) {
            String key = en.nextElement();
            studentCardTable.get(key).setNumBoxes(boxNum);
            studentCardTable.get(key).setNumQuestions(questNum);
            studentCardTable.get(key).setWeights(weights);
        }
        close();
        GradeConfirmationForm gc = new GradeConfirmationForm(backFolderPath,
                 studentCardTable, answerKey, questNum, boxNum);
        gc.setVisible(true);
    }//GEN-LAST:event_btn_GradeActionPerformed
    // method to set the answer key
    public int[] setAnswerKey() {
        int[] answers = new int[questNum];
        for (int i = 0; i < questNum; i++) {
            for (int j = 0; j < boxNum; j++) {
                if (radioBtns[i][j].isSelected()) {
                    answers[i] = j;
                }
            }
        }
        return answers;
    }

