    private void btn_ConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ConfirmActionPerformed
        try {
            questNum = Integer.parseInt(txt_Questions.getText());
            boxNum = (int) spn_Boxes.getValue();
            pnl_AnsKey.removeAll();
            GridLayout gl = new GridLayout(questNum, boxNum + 1, 5, 5);
            pnl_AnsKey.setLayout(gl);
            radioBtns = new JRadioButton[questNum][boxNum];

            for (int i = 0; i < questNum; i++) {
                ButtonGroup rdbGroup = new ButtonGroup();
                JLabel lbl_qNum = new JLabel();
                lbl_qNum.setText("Question " + (i + 1) + ": ");
                pnl_AnsKey.add(lbl_qNum);
                for (int j = 0; j < boxNum; j++) {

                    if (j == 0) {
                        radioBtns[i][j] = new JRadioButton("A");
                    }
                    if (j == 1) {
                        radioBtns[i][j] = new JRadioButton("B");
                    }
                    if (j == 2) {
                        radioBtns[i][j] = new JRadioButton("C");
                    }
                    if (j == 3) {
                        radioBtns[i][j] = new JRadioButton("D");
                    }
                    if (j == 4) {
                        radioBtns[i][j] = new JRadioButton("E");
                    }
                    rdbGroup.add(radioBtns[i][j]);
                    pnl_AnsKey.add(radioBtns[i][j]);
                }
            }
            pnl_AnsKey.revalidate();
            pnl_AnsKey.repaint();

        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_ConfirmActionPerformed

